package com.innside.steve.model;

public class Chatbot {
	
	private String textoUsuario;
	private String textoRobot;
	private String textoSaludo;
	
	public String getTextoUsuario() {
		return textoUsuario;
	}
	public void setTextoUsuario(String textoUsuario) {
		this.textoUsuario = textoUsuario;
	}
	public String getTextoRobot() {
		return textoRobot;
	}
	public void setTextoRobot(String textoRobot) {
		this.textoRobot = textoRobot;
	}
	public String getTextoSaludo() {
		return textoSaludo;
	}
	public void setTextoSaludo(String textoSaludo) {
		this.textoSaludo = textoSaludo;
	}
	
	
	

}
